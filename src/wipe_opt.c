#include "filler.h"

void	wipe_opt(t_opt **opt)
{
	(*opt)->u = NULL;
	(*opt)->d = NULL;
	(*opt)->z = NULL;
	ft_strdel(&((*opt)->str));
	*opt = NULL;
}
