#include "libft.h"

int		ft_strcmp(char const *s1, char const *s2)
{
	size_t			i;
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	while (str1[i] == str2[i])
	{
		if (str1[i] == '\0')
			break ;
		i++;
	}
	return (str1[i] - str2[i]);
}
